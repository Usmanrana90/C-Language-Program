#include<conio.h>
#include<stdio.h>
int main()

{
	
	printf("%d\n",72);		//%d is used for decimal number
	printf("%o\n",72);   //% O is used for octal number
	printf("%x\n",72);   //%x is used for hexadecimal number
 
}



// number start with 0 is octal number
//Number start with 0x or Ox is hexadecimal number

